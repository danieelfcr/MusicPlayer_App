    private void jBImportSongActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBImportSongActionPerformed
        //Create a JFileChooser, add a filter for images and let the user choose his profile image, setting the path in the textbox.
        JFileChooser dialogue = new JFileChooser("C:\\MEIA");
        FileNameExtensionFilter filter = new FileNameExtensionFilter("Songs", "wav");
        dialogue.setFileFilter(filter);

        File songFile;
        String songPath;

        int valor = dialogue.showOpenDialog(this);

        if (valor == JFileChooser.APPROVE_OPTION) {
            songFile = dialogue.getSelectedFile();
            
            if (!"".equals(jTFSongName.getText()) && !"".equals(jTFArtist.getText())) {
               songPath = songFile.getPath();
                File newSong = new File(songPath);
                newSong.renameTo(new File("C:/MEIA/CANCIONES/"+jTFSongName.getText()+"-" + jTFArtist.getText() + ".wav"));
                jTFSongPath.setText(newSong.getAbsolutePath()); 
            }   
        }
    }//GEN-LAST:event_jBImportSongActionPerformed

    private void jBAddSongActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBAddSongActionPerformed
        String songsFilePath = "C:\\MEIA\\canciones.txt";
        String binSongsFilePath = "C:\\MEIA\\bitacora_canciones.txt";
        String descSongsFilePath = "C:\\MEIA\\desc_canciones.txt";
        String descBinSongsFilePath = "C:\\MEIA\\desc_bitacora_canciones.txt";

        File songsFile = new File(songsFilePath);
        File binSongsFile = new File(binSongsFilePath);
        File descSongsFile = new File(descSongsFilePath);
        File descBinSongsFile = new File(descBinSongsFilePath);
        
        Sequential.createFiles(songsFile, descSongsFile, binSongsFile, descBinSongsFile);
        String songPath = "C:/MEIA/CANCIONES/"+jTFSongName.getText()+"-" + jTFArtist.getText() + ".wav";
                
        if (!Sequential.isMaxReorg(binSongsFile, descBinSongsFile)) {
            addToBin(Sequential.countRegisters(songsFile)+Sequential.countRegisters(binSongsFile)+1, jTFSongName.getText(), jTFArtist.getText(), songPath, binSongsFile, descBinSongsFile);
        }
        else
        {
            Sequential.Reorganization(songsFile, binSongsFile, descSongsFile, descBinSongsFile, 6);
            addToBin(Sequential.countRegisters(songsFile)+Sequential.countRegisters(binSongsFile)+1, jTFSongName.getText(), jTFArtist.getText(), songPath, binSongsFile, descBinSongsFile);
        }
        
        showMessageDialog(null, "Canción añadida exitosamente.");
    }//GEN-LAST:event_jBAddSongActionPerformed

