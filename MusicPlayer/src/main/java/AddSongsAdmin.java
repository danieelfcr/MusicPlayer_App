    public static void addToBin(int code, String songName, String artist, String songPath, File file, File desc)
    {
         try{
            String data = code + "|" + songName + "|" + artist + "|" + songPath + "|" + AdminData.getUser() + "|" + Sequential.getDate() + "|" + "1";
        
            FileWriter fw = new FileWriter(file, true);
            BufferedWriter bw = new BufferedWriter(fw);
            bw.append(data+"\n");
            bw.close();
            
            Sequential.updateDesc(file, desc, AdminData.getUser(), 6);
            
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
    
